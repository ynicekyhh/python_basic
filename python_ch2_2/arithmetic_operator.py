# 사칙연산

print(2 * 3)
print(2 + 3)
print(2 - 3)
print(2 / 3)

print(2 // 3)   # 나눗셈의 몫만 구하기
print(2 ** 3)   # 2의 3승 구하기
print(2 % 3)

# divmod() 함수
print(divmod(2, 3)) # 몫, 나머지

# 연산자 우선순위
print(2 + 3 * 4)
print(-2 + 3 * 4)
print(-(2 + 3) * 4)

# 결합순서
print(2 ** 3 ** 4)
print((2 ** 3) ** 4)
print(2 ** (3 ** 4))
